import { cn } from "@/lib/utils";

type LogoSize = "xs" | "sm" | "md" | "lg";

type LogoProps = {
  className?: string;
  /** Render only the floral mark, no wordmark. */
  markOnly?: boolean;
  /** Size of the lockup. Default `md` (h-7). */
  size?: LogoSize;
};

const LOCKUP_SIZE: Record<LogoSize, string> = {
  xs: "h-4 w-auto", // 16px — tightest, only for ultra-compact contexts
  sm: "h-5 w-auto", // 20px — nav default
  md: "h-7 w-auto", // 28px — footer / standalone
  lg: "h-9 w-auto", // 36px — hero / brand pages
};

const MARK_SIZE: Record<LogoSize, string> = {
  xs: "h-4 w-4",
  sm: "h-5 w-5",
  md: "h-7 w-7",
  lg: "h-9 w-9",
};

/**
 * Logo — the full Fiveleaf lockup.
 *
 * Uses the brand SVG paths (5 hand-drawn frangipani petals + the "fiveleaf"
 * wordmark) inlined with `fill="currentColor"` so it inherits the surrounding
 * text colour. The wordmark is set in Inter Tight 600 to match the source
 * brand file (loaded via next/font in app/layout.tsx as --font-tight).
 */
export function Logo({ className, markOnly = false, size = "md" }: LogoProps) {
  if (markOnly) {
    return (
      <span className={cn("inline-flex items-center", className)}>
        <FloralMark className={MARK_SIZE[size]} />
      </span>
    );
  }
  return (
    <span
      aria-label="Fiveleaf"
      className={cn("inline-flex items-center", className)}
    >
      <FullLockup className={LOCKUP_SIZE[size]} />
    </span>
  );
}

